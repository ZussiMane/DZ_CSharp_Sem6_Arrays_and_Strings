Array.Reverse(words); // Используем метод Array.Reverse для переворачивания массива слов

string reversed = string.Join(" ", words); // Используем метод Join для объединения перевернутого массива слов обратно в строку, разделяя слова пробелами
// Выводим получившуюся строку в консоль
